"""
Este programa é um conversor de graus Kelvin para Celsius

This program is converso de degrees Kelvin for Celsius
"""
print("Este é um conversor de Temperatura de Kelvin para Celsius")
kel = float(input("Informe o valor da temperatura em Kelvin para a conversão: "))

cel = kel - 273.15

print("A converão de: ", kel, "Kelvin \npara: ", cel, "Celsius")
